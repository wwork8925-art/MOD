namespace backend.DTOs
{
    // -------------------------------------------------------------------------
    // بيانات التعليق — تُرسل للفرونت إند لعرضه على صفحة السكن
    // -------------------------------------------------------------------------
    public class CommentDto
    {
        // رقم التعليق
        public int Id { get; set; }

        // محتوى التعليق
        public string Text { get; set; } = string.Empty;

        // تاريخ ووقت كتابة التعليق
        public DateTime CreatedAt { get; set; }

        // رقم المستخدم الذي كتب التعليق
        public int UserId { get; set; }

        // اسم المستخدم (منسوخ هنا لتجنب طلب إضافي لقاعدة البيانات)
        public string Username { get; set; } = string.Empty;

        // اسم السكن الذي كُتب فيه التعليق
        public string HostelName { get; set; } = string.Empty;
    }

    // -------------------------------------------------------------------------
    // بيانات إنشاء تعليق جديد — يرسلها المستخدم المسجل
    // لا نقبل UserId من الفرونت إند — نأخذه من التوكن مباشرة (أكثر أماناً)
    // -------------------------------------------------------------------------
    public class CreateCommentDto
    {
        // محتوى التعليق
        public string Text { get; set; } = string.Empty;

        // اسم السكن المراد التعليق عليه
        public string HostelName { get; set; } = string.Empty;
    }
}
