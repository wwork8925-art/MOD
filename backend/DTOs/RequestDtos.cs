// =============================================================================
// RequestDtos.cs — حاويات بيانات طلبات السكن
// =============================================================================
// يتقدم المستخدم بطلب للحصول على سكن، ثم يوافق/يرفض الأدمن.
// هذه الحاويات تدير تبادل البيانات لهذه العملية.
// =============================================================================

namespace backend.DTOs
{
    // -------------------------------------------------------------------------
    // بيانات الطلب — تُرسل للأدمن لعرض قائمة الطلبات
    // -------------------------------------------------------------------------
    public class HostelRequestDto
    {
        // رقم الطلب
        public int Id { get; set; }

        // رقم المستخدم الذي تقدم بالطلب
        public int UserId { get; set; }

        // اسم المستخدم (للعرض في لوحة الأدمن)
        public string Username { get; set; } = string.Empty;

        // اسم السكن المطلوب
        public string HostelName { get; set; } = string.Empty;

        // الحالة: "Pending" (قيد الانتظار) | "Approved" (موافَق) | "Rejected" (مرفوض)
        public string Status { get; set; } = string.Empty;

        // تاريخ ووقت تقديم الطلب
        public DateTime CreatedAt { get; set; }
    }

    // -------------------------------------------------------------------------
    // بيانات إنشاء طلب جديد — يرسلها المستخدم
    // لا نقبل UserId من الفرونت إند — نأخذه من التوكن (أكثر أماناً)
    // -------------------------------------------------------------------------
    public class CreateHostelRequestDto
    {
        // اسم السكن المراد طلبه
        public string HostelName { get; set; } = string.Empty;
    }

    // -------------------------------------------------------------------------
    // تحديث حالة الطلب — يرسله الأدمن (موافقة أو رفض)
    // -------------------------------------------------------------------------
    public class UpdateRequestStatusDto
    {
        // "Approved" = موافقة | "Rejected" = رفض
        public string Status { get; set; } = string.Empty;
    }
}
